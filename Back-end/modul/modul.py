def uchburchak_per(a,b,c):
    """Uchburchakni peremetrini xisoblovchi funkiya"""
    u_per = a + b + c
    return u_per

def togritortburchak_per(a,b):
   """To'g'ri to'rtburchakni peremetrini xisoblovchi funkiya"""
   tt_per = 2 * (a + b)
   return tt_per
